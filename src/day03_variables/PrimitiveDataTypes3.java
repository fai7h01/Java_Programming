package day03_variables;

public class PrimitiveDataTypes3 {
    public static void main(String[] args) {

                                        // DataType variableName = Data;
        int ch = 'M';    // 4 bytes
        char ch1 = 43;      // 2 bytes
        System.out.println(ch);
        System.out.println(ch1);

        System.out.println();

        char a = '@';
        char b = 65535;
        System.out.println(a);
        System.out.println(b);

        int sum = 'A' + 'D';
        System.out.println(sum);

        System.out.println("--------------------------");

        // boolean r1 = 123;
        // boolean r2 = 2.5;
        // boolean r3 = "Java";

        boolean r4 = true;
        boolean r5 = false;
        
        boolean r6 = 100 > 10;
        boolean r7 = 12 <= 5;

        System.out.println(r6);
        System.out.println(r7);







    }
}
