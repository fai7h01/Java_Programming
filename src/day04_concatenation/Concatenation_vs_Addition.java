package day04_concatenation;

public class Concatenation_vs_Addition {
    public static void main(String[] args) {
        // Concatenation ===>
        System.out.println("234" + 34); // "23434"
        System.out.println(10 + "11"); // "1011"
        System.out.println("12" + "23"); // "1223"

        System.out.println("=========================================");

        // Addition ===>
        System.out.println(12 + 423); //435

        System.out.println("=========================================");

        System.out.println("The value is " + true); // "The value is true"






    }
}
